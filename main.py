vbm_candidates = occ_E[occ_dos > threshold]
E_VBM = float(vbm_candidates[-1]) if len(vbm_candidates) > 0 else E_F_active - 2.0
print(f"  E_VBM (vacuum scale) = {E_VBM:.4f} eV")

# ---- 2g. Robust PDOS extraction (metal d-band, O 2p-band) ----
def extract_orbital_dos(dos_obj, element_symbol, orbital_type):
    """
    Sum PDOS for a given element + orbital type across all spins/sites.
    Raises a visible warning (not a silent except) if nothing is found.
    """
    el = Element(element_symbol)
    pdos_sum = np.zeros(len(dos_obj.energies))
    found = False
    for site, site_dos in dos_obj.pdos.items():
        if site.specie == el:
            for orb, spin_dict in site_dos.items():
                if orb.orbital_type == orbital_type:
                    for spin, arr in spin_dict.items():
                        pdos_sum += np.array(arr)
                        found = True
    if not found:
        print(f"  [Warning] No {orbital_type} PDOS found for element '{element_symbol}'.")
    return pdos_sum

d_dos_raw = extract_orbital_dos(dos_data, metal_symbol, OrbitalType.d)

