def integrate_window(energies, dos_arr, E_low, E_high):
    mask = (energies >= E_low) & (energies <= E_high)
    if mask.sum() < 2: return 0.0
    return float(_trapz(dos_arr[mask], energies[mask]))

# E_F_active is now lower (more negative) on vacuum scale
delta_E_CH4   = E_F_active - E_HOMO_CH4
delta_E_CH3OH = E_F_active - E_HOMO_CH3OH

# Calculate and store individual integrals for the report
I_d_CH4 = integrate_window(energies_abs, d_dos_raw, E_HOMO_CH4, E_F_active)
I_p_CH4 = integrate_window(energies_abs, p_dos_raw, E_HOMO_CH4, E_F_active)
I_tot_CH4 = I_d_CH4 + I_p_CH4

I_d_CH3OH = integrate_window(energies_abs, d_dos_raw, E_HOMO_CH3OH, E_F_active)
I_p_CH3OH = integrate_window(energies_abs, p_dos_raw, E_HOMO_CH3OH, E_F_active)
I_tot_CH3OH = I_d_CH3OH + I_p_CH3OH

S_kinetic = I_tot_CH4 / (I_tot_CH3OH + 1e-20)

print(f"  ΔE_CH4: {delta_E_CH4:.3f} eV | ΔE_CH3OH: {delta_E_CH3OH:.3f} eV")
print(f"  Integrals (CH4): d={I_d_CH4:.4f}, p={I_p_CH4:.4f}")
print(f"  Selectivity Index S_kinetic: {S_kinetic:.4f}")
if S_kinetic > 1:
    print("    → SELECTIVE ✓")
else:
    print("    → NON-SELECTIVE ✗")

